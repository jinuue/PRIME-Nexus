import fs from 'fs/promises';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const DEFAULT_INPUT = path.resolve(__dirname, '..', 'seed', 'store.json');
const DEFAULT_OUTPUT = path.resolve(__dirname, '..', 'db', 'migrations', '005_seed_local_data.sql');
const ROOT_DIR = path.resolve(__dirname, '..');

const args = process.argv.slice(2);
let inputPath = DEFAULT_INPUT;
let outputPath = DEFAULT_OUTPUT;

for (const arg of args) {
  if (arg.startsWith('--out=')) {
    outputPath = path.resolve(arg.slice('--out='.length));
  } else if (!arg.startsWith('--')) {
    inputPath = path.resolve(arg);
  }
}

async function loadJson(filePath) {
  const raw = await fs.readFile(filePath, 'utf8');
  return JSON.parse(raw);
}

function normalizePayload(payload) {
  const data = payload && typeof payload === 'object' ? payload : {};
  return {
    users: Array.isArray(data.users) ? data.users : [],
    applications: Array.isArray(data.applications) ? data.applications : [],
    dtrEntries: Array.isArray(data.dtrEntries) ? data.dtrEntries : [],
    schoolActivities: Array.isArray(data.schoolActivities) ? data.schoolActivities : [],
    messages: Array.isArray(data.messages) ? data.messages : [],
    emailTemplates: Array.isArray(data.emailTemplates) ? data.emailTemplates : [],
    companyDocuments: Array.isArray(data.companyDocuments) ? data.companyDocuments : [],
    deptSlots: data.deptSlots && typeof data.deptSlots === 'object' ? data.deptSlots : {},
    quarterSettings: data.quarterSettings && typeof data.quarterSettings === 'object'
      ? data.quarterSettings
      : { current: null },
  };
}

function escapeSqlString(value) {
  return String(value).replace(/'/g, "''");
}

function sqlLiteral(value) {
  if (value === null || value === undefined) return 'NULL';
  if (typeof value === 'number' && Number.isFinite(value)) return String(value);
  if (typeof value === 'boolean') return value ? 'TRUE' : 'FALSE';
  return `'${escapeSqlString(value)}'`;
}

function jsonbLiteral(value) {
  return `${sqlLiteral(JSON.stringify(value ?? null))}::jsonb`;
}

function buildConflictClause(conflict) {
  if (!conflict || !conflict.columns || !conflict.columns.length) return '';
  let clause = `ON CONFLICT (${conflict.columns.join(', ')}) `;
  if (conflict.action === 'update' && conflict.updateColumns && conflict.updateColumns.length) {
    const updates = conflict.updateColumns.map(col => `${col} = EXCLUDED.${col}`).join(', ');
    clause += `DO UPDATE SET ${updates}`;
  } else {
    clause += 'DO NOTHING';
  }
  return clause;
}

function buildInsert(table, columns, rows, conflict) {
  if (!rows.length) {
    return `-- ${table}: no rows\n`;
  }
  const valuesSql = rows
    .map(row => `  (${columns.map(col => row[col] ?? 'NULL').join(', ')})`)
    .join(',\n');
  const conflictClause = buildConflictClause(conflict);
  let sql = `INSERT INTO ${table} (${columns.join(', ')})\nVALUES\n${valuesSql}\n`;
  if (conflictClause) {
    sql += `${conflictClause}\n`;
  }
  sql += ';\n';
  return sql;
}

async function main() {
  const payload = normalizePayload(await loadJson(inputPath));

  const hrUsers = payload.users.filter(user => user.role === 'hr');
  const regularUsers = payload.users.filter(user => user.role !== 'hr');

  const sections = [];
  const displayInput = path.relative(ROOT_DIR, inputPath) || inputPath;
  sections.push(
    `-- Migration: seed local data exported from localStorage\n-- Generated by scripts/generate-seed-migration.js from ${displayInput}`
  );

  sections.push(
    `ALTER TABLE applications\n` +
    `  ADD COLUMN IF NOT EXISTS interview_date date,\n` +
    `  ADD COLUMN IF NOT EXISTS interview_time time,\n` +
    `  ADD COLUMN IF NOT EXISTS final_interview_date date,\n` +
    `  ADD COLUMN IF NOT EXISTS final_interview_time time,\n` +
    `  ADD COLUMN IF NOT EXISTS is_deployed boolean default false;\n`
  );

  sections.push(buildInsert(
    'hr_users',
    ['id', 'email', 'password', 'name', 'phone'],
    hrUsers.map(user => ({
      id: sqlLiteral(user.id),
      email: sqlLiteral(user.email),
      password: sqlLiteral(user.password),
      name: sqlLiteral(user.name),
      phone: sqlLiteral(user.phone),
    })),
    { columns: ['id'], action: 'nothing' }
  ));

  sections.push(buildInsert(
    'users',
    ['id', 'email', 'password', 'name', 'phone', 'role'],
    regularUsers.map(user => ({
      id: sqlLiteral(user.id),
      email: sqlLiteral(user.email),
      password: sqlLiteral(user.password),
      name: sqlLiteral(user.name),
      phone: sqlLiteral(user.phone),
      role: sqlLiteral(user.role),
    })),
    { columns: ['id'], action: 'nothing' }
  ));

  sections.push(buildInsert(
    'applications',
    [
      'id',
      'user_id',
      'name',
      'email',
      'phone',
      'course',
      'school',
      'cv_name',
      'cover_name',
      'ojt_type',
      'hours_required',
      'source',
      'status',
      'applied_date',
      'quarter',
      'department',
      'supervisor',
      'schedule',
      'start_date',
      'company_docs',
      'school_docs',
      'interview_date',
      'interview_time',
      'final_interview_date',
      'final_interview_time',
      'is_deployed',
    ],
    payload.applications.map(app => ({
      id: sqlLiteral(app.id),
      user_id: sqlLiteral(app.userId),
      name: sqlLiteral(app.name),
      email: sqlLiteral(app.email),
      phone: sqlLiteral(app.phone),
      course: sqlLiteral(app.course),
      school: sqlLiteral(app.school),
      cv_name: sqlLiteral(app.cvName),
      cover_name: sqlLiteral(app.coverName),
      ojt_type: sqlLiteral(app.ojtType),
      hours_required: sqlLiteral(app.hoursRequired),
      source: sqlLiteral(app.source),
      status: sqlLiteral(app.status),
      applied_date: sqlLiteral(app.appliedDate),
      quarter: sqlLiteral(app.quarter),
      department: sqlLiteral(app.department),
      supervisor: sqlLiteral(app.supervisor),
      schedule: sqlLiteral(app.schedule),
      start_date: sqlLiteral(app.startDate),
      company_docs: jsonbLiteral(app.companyDocs || {}),
      school_docs: jsonbLiteral(app.schoolDocs || []),
      interview_date: sqlLiteral(app.interviewDate),
      interview_time: sqlLiteral(app.interviewTime),
      final_interview_date: sqlLiteral(app.finalInterviewDate),
      final_interview_time: sqlLiteral(app.finalInterviewTime),
      is_deployed: sqlLiteral(app.isDeployed ?? false),
    })),
    { columns: ['id'], action: 'nothing' }
  ));

  sections.push(buildInsert(
    'dtr_entries',
    ['id', 'app_id', 'date', 'time_in', 'time_out', 'type'],
    payload.dtrEntries.map(entry => ({
      id: sqlLiteral(entry.id),
      app_id: sqlLiteral(entry.appId),
      date: sqlLiteral(entry.date),
      time_in: sqlLiteral(entry.timeIn),
      time_out: sqlLiteral(entry.timeOut),
      type: sqlLiteral(entry.type),
    })),
    { columns: ['id'], action: 'nothing' }
  ));

  sections.push(buildInsert(
    'school_activities',
    ['id', 'app_id', 'name', 'description', 'status', 'type'],
    payload.schoolActivities.map(activity => ({
      id: sqlLiteral(activity.id),
      app_id: sqlLiteral(activity.appId),
      name: sqlLiteral(activity.name),
      description: sqlLiteral(activity.description),
      status: sqlLiteral(activity.status),
      type: sqlLiteral(activity.type),
    })),
    { columns: ['id'], action: 'nothing' }
  ));

  sections.push(buildInsert(
    'messages',
    ['id', 'app_id', 'sender', 'text', 'time'],
    payload.messages.map(message => ({
      id: sqlLiteral(message.id),
      app_id: sqlLiteral(message.appId),
      sender: sqlLiteral(message.from),
      text: sqlLiteral(message.text),
      time: sqlLiteral(message.time),
    })),
    { columns: ['id'], action: 'nothing' }
  ));

  sections.push(buildInsert(
    'email_templates',
    ['id', 'name', 'subject', 'body'],
    payload.emailTemplates.map(template => ({
      id: sqlLiteral(template.id),
      name: sqlLiteral(template.name),
      subject: sqlLiteral(template.subject),
      body: sqlLiteral(template.body),
    })),
    { columns: ['id'], action: 'nothing' }
  ));

  sections.push(buildInsert(
    'company_documents',
    ['id', 'name', 'description', 'type'],
    payload.companyDocuments.map(doc => ({
      id: sqlLiteral(doc.id),
      name: sqlLiteral(doc.name),
      description: sqlLiteral(doc.description),
      type: sqlLiteral(doc.type),
    })),
    { columns: ['id'], action: 'nothing' }
  ));

  sections.push(buildInsert(
    'dept_slots',
    ['department', 'slots'],
    Object.entries(payload.deptSlots).map(([department, slots]) => ({
      department: sqlLiteral(department),
      slots: sqlLiteral(slots),
    })),
    { columns: ['department'], action: 'update', updateColumns: ['slots'] }
  ));

  sections.push(buildInsert(
    'quarter_settings',
    ['id', 'current'],
    payload.quarterSettings && payload.quarterSettings.current
      ? [{
        id: sqlLiteral(payload.quarterSettings.id ?? 1),
        current: sqlLiteral(payload.quarterSettings.current),
      }]
      : [],
    { columns: ['id'], action: 'update', updateColumns: ['current'] }
  ));

  const output = sections.join('\n');
  await fs.writeFile(outputPath, output, 'utf8');
  console.log(`Wrote migration to ${outputPath}`);
}

main().catch(error => {
  console.error('Failed to generate migration:', error.message);
  process.exit(1);
});
